    void listar(){
        List<EstadoMesa> lista=dao.listar();
        modelo=(DefaultTableModel)TablaEstadoMesa.getModel();
        Object[]ob=new Object[3];
        for(int i=0;i<lista.size();i++){
            ob[0]=lista.get(i).getId();
            ob[1]=lista.get(i).getNom();
            ob[2]=lista.get(i).getEstado();
            modelo.addRow(ob);
        }
        TablaEstadoMesa.setModel(modelo);
    }

