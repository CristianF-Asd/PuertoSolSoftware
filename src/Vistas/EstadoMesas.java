    private void TablaEstadoMesaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaEstadoMesaMouseClicked
        // TODO add your handling code here:
        int fila = TablaEstadoMesa.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una fila");
            
        }else {
            id = Integer.parseInt(TablaEstadoMesa.getValueAt(fila, 0).toString());
            String nom= TablaEstadoMesa.getValueAt(fila, 1).toString();
            String es= TablaEstadoMesa.getValueAt(fila, 2).toString();
            CampoNombre.setText(nom);
            BoxEstado.setSelectedItem(es);
        }
    }//GEN-LAST:event_TablaEstadoMesaMouseClicked

    
    void agregar(){
        if(CampoNombre.getText().length()==0){
            JOptionPane.showMessageDialog(this, "Debe llenar el campo de Nombre de Categoria");
        }else{
            String nombreCat = CampoNombre.getText();
            String es = BoxEstado.getSelectedItem().toString();
            Object[] ob= new Object[2];
            ob[0]=nombreCat;
            ob[1]=es;
            dao.add(ob);
        }
    }
    
    void actualizar(){
        int fila = TablaEstadoMesa.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una fila");
        }else{
            String nombreCat = CampoNombre.getText();
            String es = BoxEstado.getSelectedItem().toString();
            Object[] obj=new Object[3];
            obj[0] = nombreCat;
            obj[1] = es;
            obj[2] = id;
            dao.actualizar(obj);
        }
        
    }
    
    void eliminar(){
        int fila = TablaEstadoMesa.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una fila");
        }else{
            String es = "*";
            Object[] obj=new Object[2];
            obj[0]= es;
            obj[1]= id;
            dao.eliminar(obj);
        }
    }
    
    void nuevo(){
        CampoNombre.setText("");
        BoxEstado.setSelectedIndex(0);
        CampoNombre.requestFocus();
        
    }
    
    void limpiarTabla(){
        for(int i=0; i<modelo.getRowCount();i++){
            modelo.removeRow(i);
            i=i-1;  
        }
    }
    
    
