    void agregarProducto()
    {        
        item++;
        modelo=(DefaultTableModel)TablaProductos.getModel();
        String ids=CampoCodigo.getText();
        String nomb= CampoProducto.getText();
        double pre=Double.parseDouble(CampoPrecio.getText());
        int cant = Integer.parseInt(SnipperCantidad.getValue().toString());
        double total=pre*cant;
        
        ArrayList lista = new ArrayList();
        lista.add(item);
        lista.add(ids);
        lista.add(nomb);
        lista.add(cant);
        lista.add(pre);
        lista.add(total);
        Object[] ob = new Object[6];
        ob[0]=lista.get(0);
        ob[1]=lista.get(1);
        ob[2]=lista.get(2);
        ob[3]=lista.get(3);
        ob[4]=lista.get(4);
        ob[5]=lista.get(5);
        modelo.addRow(ob);
        TablaProductos.setModel(modelo);
        CalcularTotal();
    }
    
    void CalcularTotal(){
         ttotal=0;
         for(int i=0; i<TablaProductos.getRowCount();i++){
             cantTotal = Integer.parseInt(TablaProductos.getValueAt(i, 3).toString());
             preTotal = Double.parseDouble(TablaProductos.getValueAt(i, 4).toString());
             ttotal=ttotal+cantTotal*preTotal;
         } 
         
         CampoTotal.setText(""+ttotal);
    }
    
    
