    void guardarDetallePedido(){
        
        ped.updateTotal(Double.parseDouble(CampoTotal.getText()), ModificarPedidoMesa.pe2.getId());
        
        for(int i=0;i<lista.size();i++){
            if(prod.getCat(lista.get(i).getIdProducto())==8){
               int stock = prod.getStock(lista.get(i).getIdProducto());
                //System.out.print(stock);
                prod.actualizarStock(lista.get(i).getIdProducto(), stock+lista.get(i).getCantidad());                
            }                                                                                                
        }
        
        ped.eliminarPedidoDetalle(ModificarPedidoMesa.pe2.getId());
        
       
        int idve = ModificarPedidoMesa.pe2.getId();
        for(int i = 0; i<TablaProductos.getRowCount();i++){
            int idp = Integer.parseInt(TablaProductos.getValueAt(i,1).toString());
            int cant = Integer.parseInt(TablaProductos.getValueAt(i,3).toString());
            double pre = Double.parseDouble(TablaProductos.getValueAt(i, 5).toString());
            String es = "A";
            
            dp.setDetallePed(idve);
            dp.setIdProducto(idp);
            dp.setCantidad(cant);
            dp.setTotal(pre);
            dp.setEstado(es);
            //dp.setFecha(fecha_actual.getCalendarType());
            ped.GuardarDetallePedido(dp); 
            
            
            
            if(prod.getCat(idp)==8){
       
                int stock = prod.getStock(idp);
               
                prod.actualizarStock(idp, stock-cant);
            }                      
        }   
    }
    
    
