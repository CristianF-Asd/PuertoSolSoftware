    private void TablaProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaProductosMouseClicked
        // TODO add your handling code here:
        int fila = TablaProductos.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una fila");
            
        }else {
            id = Integer.parseInt(TablaProductos.getValueAt(fila, 0).toString());
            String nom= TablaProductos.getValueAt(fila, 1).toString();
            String precio= TablaProductos.getValueAt(fila, 2).toString();
            String cat= TablaProductos.getValueAt(fila, 3).toString();
            String es= TablaProductos.getValueAt(fila, 5).toString();
            String st = TablaProductos.getValueAt(fila, 4).toString();
            String des=TablaProductos.getValueAt(fila, 6).toString();
            
            CampoNombre.setText(nom);
            CampoPrecio.setText(precio);
            CategoriaBox.setSelectedItem(cat);
            EstadoBox.setSelectedItem(es);
            CampoDescripcion.setText(des);
            CampoStock.setText(st);
            
            ActualizarButton.setEnabled(true);
            EliminarButton.setEnabled(true);
            AgregarButton.setEnabled(false);
            NuevoButton.setEnabled(true);
            
            
        } 
    }//GEN-LAST:event_TablaProductosMouseClicked

