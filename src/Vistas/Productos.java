    void agregar(){
        if(CampoNombre.getText().length()==0 || CampoPrecio.getText().length()==0 ){
            JOptionPane.showMessageDialog(this, "Debe llenar los campos");
        }else{
            List<Categoria> listac = dao.listarCat();
            String nombrePro = CampoNombre.getText();
            String precio = CampoPrecio.getText();
            String des = CampoDescripcion.getText();
            String es = EstadoBox.getSelectedItem().toString();
            String st = CampoStock.getText();
            int cat = listac.get(CategoriaBox.getSelectedIndex()).getId();
            
            Object[] ob= new Object[6];
            ob[0]=nombrePro;
            ob[1]=des;
            ob[2]=precio;
            ob[3]=cat;
            ob[4]=es;
            ob[5]=st;
            dao.add(ob);
        }
    }
    
    void actualizar(){
        int fila = TablaProductos.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una fila");
        }else{
            
            List<Categoria> listac = dao.listarCat();
            String nombrePro = CampoNombre.getText();
            String precio = CampoPrecio.getText();
            String des = CampoDescripcion.getText();
            String es = EstadoBox.getSelectedItem().toString();
            String st = CampoStock.getText();
            int cat = listac.get(CategoriaBox.getSelectedIndex()).getId();
            
            Object[] ob= new Object[7];
            ob[0]=nombrePro;
            ob[1]=des;
            ob[2]=precio;
            ob[3]=cat;
            ob[4]=es;
            ob[5]=st;
            ob[6]=id;
            dao.actualizar(ob);
       
        }
        
    }
    
    void eliminar(){
        int fila = TablaProductos.getSelectedRow();
        if(fila==-1){
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una fila");
        }else{
            String es = "*";
            Object[] obj=new Object[2];
            obj[0]= es;
            obj[1]= id;
            dao.eliminar(obj);
        }
    }
    
    void nuevo(){
        CampoNombre.setText("");
        CampoDescripcion.setText("");
        CampoPrecio.setText("");
        CampoStock.setText("0");
        EstadoBox.setSelectedIndex(0);
        CategoriaBox.setSelectedIndex(0);
        CampoNombre.requestFocus();
        
    }
    void limpiarTabla(){
        for(int i=0; i<modelo.getRowCount();i++){
            modelo.removeRow(i);
            i=i-1;  
        }
    }
    

