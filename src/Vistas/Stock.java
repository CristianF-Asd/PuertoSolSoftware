    private void BotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarActionPerformed
        // TODO add your handling code here:
        
        if(TablaStock.getRowCount()==0 ){
           JOptionPane.showMessageDialog(this, "INGRESE LOS DATOS"); 
        }else{
            guardarRegistroStock();        
            JOptionPane.showMessageDialog(this, "STOCK REGISTRADO");
            actualizarstock();
            dispose(); 
        }       
    }//GEN-LAST:event_BotonGuardarActionPerformed

    void guardarRegistroStock(){
        
       
        for(int i = 0; i<TablaStock.getRowCount();i++){
            int idp = Integer.parseInt(TablaStock.getValueAt(i,0).toString());
            int cant = Integer.parseInt(TablaStock.getValueAt(i,4).toString());
            
            rs.setIdProducto(idp);
            rs.setCantidad(cant);
            
            //dp.setFecha(fecha_actual.getCalendarType());
             rsd.add(rs);
        } 
    }
    void actualizarstock(){
        for(int i = 0; i<TablaStock.getRowCount();i++){
            int idp = Integer.parseInt(TablaStock.getValueAt(i,0).toString());
            int cantActual = Integer.parseInt(TablaStock.getValueAt(i,3).toString());
            int cantAdicional = Integer.parseInt(TablaStock.getValueAt(i,4).toString());
            
            
            dao.actualizarStock(idp, cantActual+cantAdicional);
            
            //dp.setFecha(fecha_actual.getCalendarType());
        }
        
    }
    
