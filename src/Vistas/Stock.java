    void agregarProducto(){    
           
        
        modelo2=(DefaultTableModel)TablaStock.getModel();
        String ids=CampoId.getText();
        String nomb= CampoNombre.getText();
        String prec= CampoPrecio.getText();
        String stockactual= CampoStock.getText();
        int  stock=Integer.parseInt(CampoIngresar.getText());
        
        
        
        ArrayList lista = new ArrayList();
        lista.add(ids);
        lista.add(nomb);
        lista.add(prec);
        lista.add(stockactual);
        lista.add(stock);
        Object[] ob = new Object[5];
        ob[0]=lista.get(0);
        ob[1]=lista.get(1);
        ob[2]=lista.get(2);
        ob[3]=lista.get(3);
        ob[4]=lista.get(4);

        modelo2.addRow(ob);
        TablaStock.setModel(modelo2);
        
        CampoId.setText("");
        CampoNombre.setText("");
        CampoPrecio.setText("");
        CampoStock.setText("");
        CampoIngresar.setText("");

    
    }
